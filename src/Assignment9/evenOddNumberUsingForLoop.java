package Assignment9;

public class evenOddNumberUsingForLoop {

	public static void main(String[] args) {
		for (int i=1; i<=100;i++) {
			if (i% 2 !=0);
			 System.out.println("Odd numbers from 1 to 100:  "+ i);
		}
		
		
		System.out.println();
		
		for(int num= 1000; num<=1099; num++ ) {
			if (num % 2 ==0) 
		        System.out.println("Even numbers from 1000 to 1099:   "+ num);
		        	
		}
		
		
		
	}

}
