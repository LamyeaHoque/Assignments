package assignment8;

public class multiplicationTable {

	public static void main(String[] args) {
		int num = 7;
		 
        int range = 10;
 
        int i = 1;
 
        while (i <= range) {
 
           
            System.out.println (num + " * " + i + " = " + num * i);
            i++;

	}

}
}