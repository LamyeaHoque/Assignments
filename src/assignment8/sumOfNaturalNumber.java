package assignment8;

public class sumOfNaturalNumber {

	public static void main(String[] args) {
		int n = 300, sum = 0, i = 200;
		 
       
        while (i <= n) {
 
            sum = sum + i;
            i++;
        }
 
        
        System.out.println("Sum of natural numbers using while loop is:  " + sum);

	}

}
